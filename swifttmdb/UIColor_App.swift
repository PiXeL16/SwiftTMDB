import UIKit

extension UIColor {
    
    static func tintColor() -> UIColor {
        
        return UIColor(hexString: "#FF9800")!
        
    }
    
    static func accentColor() -> UIColor {
        
        return UIColor(hexString: "#00BCD4")!
    }

    
}